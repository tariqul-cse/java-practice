package chapter4;

public class J04_04 {
    public static void main(String arg[]){

        /*Prefix and Postfi notations of Incremental Operator*/

        int counter = 0;
        System.out.println("Counter is: " +counter);
        System.out.println("\n++Counter is : " + ++counter);
        System.out.println("\nCounter is : " + counter);

        counter = 0;
        System.out.println("\n\nCounter is: " + counter);
        System.out.println("\nCounter++ is: " + counter++);
        System.out.println("\nCounter is: " + counter);

    }
}
